package _19interface;

// Interface estipula métodos obrigatórios.
// Toda classe que implementar uma interface, obrigatoriamente deverá ter esses métodos.
// Interface permite o uso de herança múltipla.

interface CalculadoraSimples {

    public int somar(int numero1, int numero2);
    public int subtrair(int numero1, int numero2);
}
